struct Point {
    var x: Int
    var y: Int
}

struct Room {
    var width: Int
    var height: Int
    var char: Point
    
    enum Moving {
        case forward
        case down
        case left
        case right
    }
    
    func showRoom() {
        
        var cell = String()
        
        for w in 1...width {
            cell = ""
            for h in 1...height {
                
                if char.x == h && char.y == w {
                    cell += " üòÅ"
                } else {
                    cell += " ‚¨úÔ∏è"
                }
            }
            print(cell)
        }
    }
    
    mutating func moveChar(movingEnum: Moving) {
        
        switch movingEnum {
        case .forward:
            char.y -= 1
        case .down:
            if char.y < height {
                char.y += 1
            }
        case .left:
            char.x -= 1
        case .right:
            if char.x < width {
            char.x += 1
            }
        }
    }
}

